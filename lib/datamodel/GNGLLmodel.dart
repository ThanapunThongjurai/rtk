class GNGSTmodel{
  String? lat;
  String? latDir;
  String? lonDir;
  String? lon;
  String? utc;
  String? dataStatus;
  String? ind;

  @override
  String toString() {
    return 'GNGSTmodel{lat: $lat, latDir: $latDir, lonDir: $lonDir, lon: $lon, utc: $utc, dataStatus: $dataStatus, ind: $ind}';
  }
}