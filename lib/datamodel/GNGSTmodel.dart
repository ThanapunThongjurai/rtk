class GNGSTmodel{
  String? utc;
  String? rms;
  String? smjr;
  String? smnr;
  String? orient;
  String? lat;
  String? lon;
  String? alt;

  @override
  String toString() {
    return 'GNGSTmodel{utc: $utc, rms: $rms, smjr: $smjr, smnr: $smnr, orient: $orient, lat: $lat, lon: $lon, alt: $alt}';
  }
}